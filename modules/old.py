async def roll(client: Client, message: Message):
    roll, frm, to = message.command
    await message.edit(f'Вы получили число {r.randint(int(frm), int(to))} [`{frm}`-`{to}`]')

async def trry(client: Client, message: Message):
    random = r.randint(0, 1)
    if random == 1: tr = '✅'
    else: tr = '🚫'
    await message.edit(f'{message.split(" ",1).capitalize()}: {tr}')

